namespace Projekt120;

public class BuchstabenFunktionen
{
    public static bool EinBuchstabeLesen(string? text, int position, out char zeichen)
    {
        _ = text;
        _ = position;
        zeichen = ' ';
        return true;
    }

    public static bool ZweiBuchstabenTauschen(ref string? text, int position1, int position2)
    {
        _ = text;
        _ = position1;
        _ = position2;
        return true;
    }
}
